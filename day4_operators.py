x = 8
y = 4

# Arithmetic
print("Addition:", x + y)
print("Power:", x ** y)

# Comparison
print("x equal to y?", x == y)
print("x greater than y?", x > y)

# Logical
print("x > 5 and y < 10:", x > 5 and y < 10)

# Arithmetic Operators: +, -, *, /, %, //, **
# Comparison Operators: ==, !=, >, <, >=, <=
# Logical Operators: and, or, not
# Comparison aur Logical operators ka result hamesha True ya False hota hai

